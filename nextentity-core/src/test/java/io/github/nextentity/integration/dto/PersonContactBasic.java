package io.github.nextentity.integration.dto;

import io.github.nextentity.core.annotation.EntityPath;

public class PersonContactBasic {

    private Long id;
    private String name;
    @EntityPath("contactInfo.email")
    private String email;
    @EntityPath("contactInfo.phone")
    private String phone;
    @EntityPath("contactInfo.address.street")
    private String street;
    @EntityPath("contactInfo.address.city")
    private String city;
    @EntityPath("contactInfo.address.zipCode")
    private String zipCode;

    public PersonContactBasic() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getZipCode() {
        return zipCode;
    }

    public void setZipCode(String zipCode) {
        this.zipCode = zipCode;
    }

    @Override
    public String toString() {
        return "PersonContactBasic{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                ", phone='" + phone + '\'' +
                ", street='" + street + '\'' +
                ", city='" + city + '\'' +
                ", zipCode='" + zipCode + '\'' +
                '}';
    }
}
