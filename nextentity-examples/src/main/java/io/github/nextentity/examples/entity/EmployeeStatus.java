package io.github.nextentity.examples.entity;

/**
 * Employee status enum for examples.
 * <p>
 * Demonstrates enum handling in queries and entities.
 */
public enum EmployeeStatus {

    /**
     * Active employee.
     */
    ACTIVE,

    /**
     * Inactive employee.
     */
    INACTIVE,

    /**
     * Employee on leave.
     */
    ON_LEAVE,

    /**
     * Employee terminated.
     */
    TERMINATED

}